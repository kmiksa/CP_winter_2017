package pl.waw.sgh.shapes;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

public class CompareShapes {

    public static void main(String[] args) {
        Rectangle r1 = new Rectangle(new Params(4,5));
        Circle c1 = new Circle(new Params(2));
        Circle c2 = new Circle(new Params(5));

        System.out.println(r1.compareTo(c1));
        System.out.println("R1: " + r1.calcSurface());
        System.out.println("C1: " + c1.calcSurface());
        System.out.println("C2: " + c2.calcSurface());
        System.out.println(r1.compareTo(c2));

        List<Shape> shapeList = new ArrayList<>();
        shapeList.add(r1);
        shapeList.add(c1);
        shapeList.add(c2);

        Collections.sort(shapeList);

        System.out.println(shapeList);


    }
}
